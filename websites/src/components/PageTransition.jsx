import { motion } from 'framer-motion';

/**
 * Wraps any page content with smooth enter/exit transitions.
 * - Enter: Fades in + slides up slightly
 * - Exit:  Fades out + slides down slightly
 * - Duration is kept short (0.3s) so it feels snappy, not sluggish.
 */
const pageVariants = {
  initial: {
    opacity: 0,
    y: 12,
  },
  animate: {
    opacity: 1,
    y: 0,
    transition: {
      duration: 0.3,
      ease: [0.25, 0.1, 0.25, 1],
    },
  },
  exit: {
    opacity: 0,
    y: -8,
    transition: {
      duration: 0.2,
      ease: [0.25, 0.1, 0.25, 1],
    },
  },
};

export default function PageTransition({ children }) {
  return (
    <motion.div
      variants={pageVariants}
      initial="initial"
      animate="animate"
      exit="exit"
    >
      {children}
    </motion.div>
  );
}
